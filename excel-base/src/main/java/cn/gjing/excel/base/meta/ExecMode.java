package cn.gjing.excel.base.meta;

/**
 * Excel write executor mode
 *
 * @author Gjing
 **/
public enum ExecMode {
    /**
     * Binding mode write
     */
    BIND_WRITE,
    /**
     * Binding mode read
     */
    BIND_READ,
    /**
     * Simple mode write
     */
    SIMPLE_WRITE,
    /**
     * Simple mode read
     */
    SIMPLE_READ;
}
